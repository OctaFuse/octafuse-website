#!/usr/bin/env node

import { execFileSync } from 'node:child_process';
import { existsSync, mkdirSync, mkdtempSync, readFileSync, rmSync, writeFileSync } from 'node:fs';
import { dirname, join, resolve } from 'node:path';
import { tmpdir } from 'node:os';

const root = process.cwd();
const contractPath = join(root, 'sync/contract.json');
const contract = JSON.parse(readFileSync(contractPath, 'utf8'));

const args = parseArgs(process.argv.slice(2));
const ref = args.ref ?? contract.source.defaultRef ?? 'main';
const dryRun = Boolean(args.dryRun);
const gatewayDir = resolveGatewayDir(args.gatewayDir, ref);
const commit = git(gatewayDir, ['rev-parse', 'HEAD'], { fallback: ref }).trim();
const repo = contract.source.repo;
const blobBase = `https://github.com/${repo}/blob/${ref}`;

const missing = [];
for (const item of allItems(contract)) {
	const file = join(gatewayDir, item.path);
	if (!existsSync(file)) missing.push(item.path);
}

if (missing.length) {
	console.error(`Gateway sync contract references missing files at ${gatewayDir}:`);
	for (const file of missing) console.error(`- ${file}`);
	process.exit(1);
}

const outputs = [
	{
		target: contract.pages.zh.target,
		content: renderPage('zh', contract, { ref, commit, blobBase }),
	},
	{
		target: contract.pages.en.target,
		content: renderPage('en', contract, { ref, commit, blobBase }),
	},
	{
		target: 'sync/manifest.json',
		content: `${JSON.stringify(
			{
				source: repo,
				ref,
				commit,
				contract: 'sync/contract.json',
				targets: [contract.pages.zh.target, contract.pages.en.target],
				files: allItems(contract).map((item) => item.path),
			},
			null,
			2,
		)}\n`,
	},
];

let changed = false;
for (const output of outputs) {
	const current = existsSync(output.target) ? readFileSync(output.target, 'utf8') : '';
	if (current !== output.content) {
		changed = true;
		if (!dryRun) {
			mkdirSync(dirname(output.target), { recursive: true });
			writeFileSync(output.target, output.content);
		}
	}
}

cleanupGatewayDir(gatewayDir, args.gatewayDir);

if (changed) {
	console.log(dryRun ? 'Gateway docs reference is out of sync.' : 'Gateway docs reference synced.');
	process.exit(dryRun ? 2 : 0);
}

console.log('Gateway docs reference is up to date.');

function parseArgs(argv) {
	const out = {};
	for (let i = 0; i < argv.length; i += 1) {
		const arg = argv[i];
		if (arg === '--dry-run') {
			out.dryRun = true;
		} else if (arg === '--ref') {
			out.ref = argv[++i];
		} else if (arg === '--gateway-dir') {
			out.gatewayDir = argv[++i];
		} else {
			throw new Error(`Unknown argument: ${arg}`);
		}
	}
	return out;
}

function resolveGatewayDir(gatewayDirArg, gitRef) {
	if (gatewayDirArg) {
		const dir = resolve(root, gatewayDirArg);
		if (!existsSync(dir)) throw new Error(`Gateway directory not found: ${dir}`);
		return dir;
	}

	const sibling = resolve(root, '../octafuse-gateway');
	if (existsSync(sibling)) return sibling;

	const tmp = mkdtempSync(join(tmpdir(), 'octafuse-gateway-'));
	git(tmp, ['init']);
	git(tmp, ['remote', 'add', 'origin', `https://github.com/${repo}.git`]);
	git(tmp, ['fetch', '--depth', '1', 'origin', gitRef]);
	git(tmp, ['checkout', '--detach', 'FETCH_HEAD']);
	return tmp;
}

function cleanupGatewayDir(dir, explicitGatewayDir) {
	if (explicitGatewayDir) return;
	if (dirname(dir) === tmpdir() && dir.includes('octafuse-gateway-')) {
		rmSync(dir, { recursive: true, force: true });
	}
}

function git(cwd, argv, options = {}) {
	try {
		return execFileSync('git', argv, { cwd, encoding: 'utf8', stdio: ['ignore', 'pipe', 'pipe'] });
	} catch (error) {
		if (options.fallback != null) return options.fallback;
		throw error;
	}
}

function allItems(data) {
	return data.sections.flatMap((section) => section.items);
}

function renderPage(locale, data, meta) {
	const page = data.pages[locale];
	const title = page.title;
	const description = page.description;
	const generated = locale === 'zh' ? '此页由 `npm run sync:gateway` 生成。' : 'This page is generated by `npm run sync:gateway`.';
	const trust =
		locale === 'zh'
			? '如果官网摘要与 gateway 仓库文档不一致，请以 gateway 仓库为准。'
			: 'If this site and the gateway repository disagree, trust the gateway repository.';
	const startTitle = locale === 'zh' ? '使用路径建议' : 'Suggested path';
	const startBody =
		locale === 'zh'
			? '[使用 OctaFuse](/zh/docs/quick-start/) 请优先阅读官网的快速开始和使用指南；需要 API、迁移、部署 runbook 或排障细节时，再打开下方技术参考。'
			: 'For day-to-day use, start with this site’s [Quick start](/en/docs/quick-start/) and user guides. Open the technical references below for APIs, migrations, deployment runbooks, or troubleshooting details.';

	return `---\ntitle: ${quoteYaml(title)}\ndescription: ${quoteYaml(description)}\n---\n\nimport { Aside, CardGrid, LinkCard } from '@astrojs/starlight/components';\n\n${page.intro}\n\n<Aside type="tip" title="${startTitle}">\n\t${startBody}\n</Aside>\n\n<Aside type="note" title="Source">\n\t${generated} ${trust}\n\n\tGateway ref: \`${meta.ref}\` · commit: \`${meta.commit}\`\n</Aside>\n\n${renderCards(locale, data, meta)}\n\n${data.sections.map((section) => renderSection(locale, section, meta)).join('\n\n')}\n`;
}

function renderCards(locale, data, meta) {
	const items = [
		data.sections[0].items[1],
		data.sections[1].items[2],
		data.sections[1].items[3],
		data.sections[3].items[2],
	];
	const cards = items
		.map((item) => {
			return `\t<LinkCard title="${escapeAttribute(item.label[locale])}" description="${escapeAttribute(item.description[locale])}" href="${meta.blobBase}/${item.path}" />`;
		})
		.join('\n');
	return `<CardGrid>\n${cards}\n</CardGrid>`;
}

function renderSection(locale, section, meta) {
	const what = locale === 'zh' ? '内容' : 'What it covers';
	const rows = section.items
		.map((item) => `| [${item.label[locale]}](${meta.blobBase}/${item.path}) | ${item.description[locale]} |`)
		.join('\n');
	return `## ${section.title[locale]}\n\n| Doc | ${what} |\n|-----|----------------|\n${rows}`;
}

function quoteYaml(value) {
	return JSON.stringify(value);
}

function escapeAttribute(value) {
	return value.replaceAll('&', '&amp;').replaceAll('"', '&quot;');
}
